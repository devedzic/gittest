public class Pear {
    
    private String sweetness;

    public Pear(String sweetness) {
        super();
        this.sweetness = sweetness;
    }

    public Pear() {
        super();
    }

    public String getSweetness() {
        return sweetness;
    }

    public void setSweetness(String sweetness) {
        this.sweetness = sweetness;
    }

}
